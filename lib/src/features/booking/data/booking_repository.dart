  Stream<Booking> getBookingStream(String bookingId) {
    return _firestore.collection('appointments').doc(bookingId).snapshots().map(
      (doc) {
        if (!doc.exists) {
          throw Exception('Agendamento não encontrado');
        }
        return Booking.fromJson({...doc.data()!, 'id': doc.id});
      },
    );
  }

  Future<void> updateBookingStatus(
    String bookingId,
    BookingStatus status, {
    String? message,
    String? actorId,
  }) {
    final Map<String, dynamic> updates = {'status': status.name};

    if (actorId != null) {
      final log = BookingLog(
        message: message ?? 'Status updated to ${status.name}',
        timestamp: DateTime.now(),
        actorId: actorId,
        status: status,
      );
      updates['logs'] = FieldValue.arrayUnion([log.toJson()]);
    }

    return _firestore.collection('appointments').doc(bookingId).update(updates);
  }

  Future<void> cancelBooking(String bookingId, {required String actorId}) {
    return updateBookingStatus(
      bookingId,
      BookingStatus.cancelled,
      message: 'Booking cancelled by user',
      actorId: actorId,
    );
  }

  Future<void> markAsRated(String bookingId, int rating, String? comment) {
    return _firestore.collection('appointments').doc(bookingId).update({
      'isRated': true,
      'rating': rating,
      'ratingComment': comment,
    });
  }

  Stream<List<Booking>> getBookingsForDate(DateTime date) {
    final startOfDay = DateTime(date.year, date.month, date.day);
    final endOfDay = startOfDay.add(const Duration(days: 1));

    return _firestore
        .collection('appointments')
        .where(
          'scheduledTime',
          isGreaterThanOrEqualTo: startOfDay.toIso8601String(),
        )
        .where('scheduledTime', isLessThan: endOfDay.toIso8601String())
        .orderBy('scheduledTime')
        .snapshots()
        .map((snapshot) {
          return snapshot.docs
              .map((doc) {
                try {
                  final data = doc.data();
                  return Booking.fromJson({
                    ...data,
                    'id': doc.id,
                    'status': data['status'] ?? 'scheduled',
                    'totalPrice':
                        (data['totalPrice'] as num?)?.toDouble() ?? 0.0,
                  });
                } catch (e) {
                  print('Error parsing booking ${doc.id}: $e');
                  return null;
                }
              })
              .whereType<Booking>()
              .toList();
        });
  }

  Future<Booking?> getLastFinishedBookingForVehicle(String vehicleId) async {
    try {
      final query = await _firestore
          .collection('appointments')
          .where('vehicleId', isEqualTo: vehicleId)
          .where('status', isEqualTo: 'finished')
          .orderBy('scheduledTime', descending: true)
          .limit(1)
          .get();

      if (query.docs.isEmpty) return null;

      final doc = query.docs.first;
      final data = doc.data();
      return Booking.fromJson({
        ...data,
        'id': doc.id,
        'status': data['status'] ?? 'scheduled',
        'totalPrice': (data['totalPrice'] as num?)?.toDouble() ?? 0.0,
      });
    } catch (e) {
      print('Error fetching last booking for vehicle $vehicleId: $e');
      return null;
    }
  }

  /// Get all bookings for a specific vehicle (for history view)
  Stream<List<Booking>> getVehicleBookings(String vehicleId) {
    return _firestore
        .collection('appointments')
        .where('vehicleId', isEqualTo: vehicleId)
        .snapshots()
        .map((snapshot) {
          final bookings = snapshot.docs
              .map((doc) {
                try {
                  final data = doc.data();
                  return Booking.fromJson({
                    ...data,
                    'id': doc.id,
                    'status': data['status'] ?? 'scheduled',
                    'totalPrice':
                        (data['totalPrice'] as num?)?.toDouble() ?? 0.0,
                  });
                } catch (e) {
                  print('Error parsing booking ${doc.id}: $e');
                  return null;
                }
              })
              .whereType<Booking>()
              .toList();

          // Sort in Dart to avoid needing a Firestore composite index
          bookings.sort((a, b) => b.scheduledTime.compareTo(a.scheduledTime));
          return bookings;
        });
  }
}

