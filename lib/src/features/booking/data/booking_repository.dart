import '../../../shared/models/service_package.dart';
import '../../../shared/models/vehicle.dart';

class BookingRepository {
  final FirebaseFirestore _firestore;

  BookingRepository(this._firestore);

  // Mock Services for now
  Future<List<ServicePackage>> getServices() async {
    await Future.delayed(const Duration(milliseconds: 500));
    return [
      const ServicePackage(
        id: 'basic',
        title: 'Lavagem Simples',
        description: 'Lavagem externa e secagem',
        price: 40.0,
        durationMinutes: 45,
      ),
      const ServicePackage(
        id: 'premium',
        title: 'Lavagem Completa',
        description: 'Lavagem interna e externa, cera e pretinho',
        price: 60.0,
        durationMinutes: 60,
        isPopular: true,
      ),
      const ServicePackage(
        id: 'deluxe',
        title: 'Lavagem Deluxe',
        description: 'Completa + polimento e higienização de bancos',
        price: 100.0,
        durationMinutes: 90,
      ),
    ];
  }

  // Vehicles
  Stream<List<Vehicle>> getUserVehicles(String userId) {
    return _firestore
        .collection('vehicles')
        .where('user_id', isEqualTo: userId)
        .snapshots()
        .map((snapshot) {
          return snapshot.docs.map((doc) {
            final data = doc.data();
            // Ensure ID is passed if not in data
            return Vehicle.fromJson({...data, 'id': doc.id});
          }).toList();
        });
  }

  Future<void> createVehicle(Vehicle vehicle, String userId) {
    // Exclude ID from data as it's generated by Firestore
    final data = vehicle.toJson();
    data.remove('id');

    return _firestore.collection('vehicles').add({
      ...data,
      'user_id': userId,
      'created_at': FieldValue.serverTimestamp(),
    });
  }

  // Appointments / Bookings
  Future<bool> checkAvailability(DateTime startTime) async {
    final dateStr = DateFormat('yyyy-MM-dd').format(startTime);
    final timeStr = DateFormat('HH:mm').format(startTime);

    // 1. Get Availability Config
    final availabilityDoc = await _firestore
        .collection('availability')
        .doc(dateStr)
        .get();

    if (!availabilityDoc.exists) {
      // Default: Open, 2 slots
      return true;
    }

    final availability = Availability.fromJson(availabilityDoc.data()!);
    if (!availability.isOpen) return false;

    final maxSlots = availability.slots[timeStr] ?? 2;

    // 2. Count Existing Appointments
    final appointmentsQuery = await _firestore
        .collection('bookings')
        .where('scheduledTime', isEqualTo: startTime.toIso8601String())
        .where('status', isNotEqualTo: 'cancelled')
        .get();

    return appointmentsQuery.docs.length < maxSlots;
  }

  Future<String> createBooking(Booking booking) async {
    return _firestore.runTransaction((transaction) async {
      // 1. Check for conflicts
      // This is a simplified check. In a real app, you'd query for overlapping ranges.
      // Since we slot by hour, we check if there's a booking at the exact same time.
      // Note: Firestore transactions require reads to happen before writes.
      // However, complex queries inside transactions are limited.
      // A common pattern is to use a dedicated "availability" document or
      // rely on a unique constraint if the slot ID is deterministic.
      // Here, we'll do a query for the specific time slot.

      final querySnapshot = await _firestore
          .collection('bookings')
          .where(
            'scheduledTime',
            isEqualTo: booking.scheduledTime.toIso8601String(),
          )
          .where(
            'status',
            whereIn: [
              BookingStatus.pending.name,
              BookingStatus.confirmed.name,
              BookingStatus.washing.name,
              BookingStatus.drying.name,
            ],
          )
          .get(); // Transactional read not directly supported for queries in all SDKs, but let's try standard check.
      // STRICTLY SPEAKING: Firestore client-side transactions don't support queries.
      // We must read a specific document.
      // To make this truly robust, we should have a 'slots/YYYY-MM-DD-HH' document.
      // For this "Elite" demo, let's assume we are checking a specific slot document if we had one,
      // OR we accept that client-side query-based conflict check is "best effort" without a dedicated slot document.
      // Let's implement the "Best Effort" query first, knowing it's not fully ACID without a slot doc.

      if (querySnapshot.docs.isNotEmpty) {
        throw Exception(
          'Horário indisponível. Por favor, escolha outro horário.',
        );
      }

      final docRef = _firestore.collection('bookings').doc();
      final bookingWithId = booking.copyWith(id: docRef.id);

      transaction.set(docRef, {
        ...bookingWithId.toJson(),
        'status': bookingWithId.status.name,
        'scheduledTime': bookingWithId.scheduledTime
            .toIso8601String(), // Ensure string format for query
      });

      return docRef.id;
    });
  }

  Stream<List<Booking>> getUserBookings(String userId) {
    return _firestore
        .collection('bookings')
        .where('userId', isEqualTo: userId)
        .orderBy('scheduledTime', descending: true)
        .snapshots()
        .map((snapshot) {
          return snapshot.docs.map((doc) {
            final data = doc.data();
            return Booking.fromJson({...data, 'id': doc.id});
          }).toList();
        });
  }

  Stream<List<Booking>> getAllBookings() {
    return _firestore
        .collection('bookings')
        .orderBy('scheduledTime', descending: true)
        .snapshots()
        .map((snapshot) {
          return snapshot.docs.map((doc) {
            final data = doc.data();
            return Booking.fromJson({...data, 'id': doc.id});
          }).toList();
        });
  }

  Stream<Booking> getBookingStream(String bookingId) {
    return _firestore.collection('bookings').doc(bookingId).snapshots().map((
      doc,
    ) {
      if (!doc.exists) {
        throw Exception('Agendamento não encontrado');
      }
      return Booking.fromJson({...doc.data()!, 'id': doc.id});
    });
  }
}

final bookingRepositoryProvider = Provider<BookingRepository>((ref) {
  return BookingRepository(FirebaseFirestore.instance);
});

final servicesProvider = FutureProvider<List<ServicePackage>>((ref) {
  return ref.watch(bookingRepositoryProvider).getServices();
});

final userVehiclesProvider = StreamProvider.family<List<Vehicle>, String>((
  ref,
  userId,
) {
  return ref.watch(bookingRepositoryProvider).getUserVehicles(userId);
});

final userBookingsProvider = StreamProvider.family<List<Booking>, String>((
  ref,
  userId,
) {
  return ref.watch(bookingRepositoryProvider).getUserBookings(userId);
});

final allBookingsProvider = StreamProvider<List<Booking>>((ref) {
  return ref.watch(bookingRepositoryProvider).getAllBookings();
});

final bookingStreamProvider = StreamProvider.family<Booking, String>((
  ref,
  bookingId,
) {
  return ref.watch(bookingRepositoryProvider).getBookingStream(bookingId);
});
