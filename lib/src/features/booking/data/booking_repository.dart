  // Appointments / Bookings
  Future<bool> checkAvailability(DateTime startTime) async {
    final dateStr = DateFormat('yyyy-MM-dd').format(startTime);
    final timeStr = DateFormat('HH:mm').format(startTime);

    // 1. Get Availability Config
    final availabilityDoc = await _firestore
        .collection('availability')
        .doc(dateStr)
        .get();

    if (!availabilityDoc.exists) {
      // Default: Open, 2 slots
      return true;
    }

    final availability = Availability.fromJson(availabilityDoc.data()!);
    if (!availability.isOpen) return false;

    final maxSlots = availability.slots[timeStr] ?? 2;

    // 2. Count Existing Appointments
    final appointmentsQuery = await _firestore
        .collection('appointments')
        .where('scheduledTime', isEqualTo: startTime.toIso8601String())
        .where('status', isNotEqualTo: 'cancelled')
        .get();

    return appointmentsQuery.docs.length < maxSlots;
  }

  Future<String> createBooking(Booking booking) async {
    print('🟢 Repository.createBooking: Starting...');
    print('   - userId: ${booking.userId}');
    print('   - scheduledTime: ${booking.scheduledTime}');

    // Note: Conflict checking removed due to Firestore permission constraints.
    // Users can only read their own appointments, not query all appointments.
    // Consider implementing conflict detection via Cloud Functions or
    // Firestore Security Rules with custom claims if needed.

    // Create Booking Document
    print('🟢 Repository: Creating document reference...');
    final docRef = _firestore.collection('appointments').doc();
    print('🟢 Repository: Generated ID = ${docRef.id}');

    final bookingWithId = booking.copyWith(id: docRef.id);
    final jsonData = bookingWithId.toJson();

    print('🟢 Repository: JSON data to save:');
    print(jsonData);

    print('🟢 Repository: Calling docRef.set()...');
    try {
      await docRef.set(jsonData);
      print('✅ Repository: Document saved successfully!');
    } catch (e, stackTrace) {
      print('❌ Repository: Failed to save document - $e');
      print('❌ Stack trace: $stackTrace');
      rethrow;
    }

    print('✅ Repository: Returning booking ID = ${docRef.id}');
    return docRef.id;
  }

  Stream<List<Booking>> getUserBookings(String userId) {
    return _firestore
        .collection('appointments')
        .where('userId', isEqualTo: userId)
        .orderBy('scheduledTime', descending: true)
        .snapshots()
        .map((snapshot) {
          return snapshot.docs
              .map((doc) {
                try {
                  final data = doc.data();
                  return Booking.fromJson({
                    ...data,
                    'id': doc.id,
                    'status': data['status'] ?? 'scheduled',
                    'totalPrice':
                        (data['totalPrice'] as num?)?.toDouble() ?? 0.0,
                  });
                } catch (e) {
                  print('Error parsing booking ${doc.id}: $e');
                  return null;
                }
              })
              .whereType<Booking>()
              .toList();
        });
  }

